    protected void loadDanhMuc()
    {
        // Bước 1: viết câu truy vấn SQL
        String query = "SELECT * FROM danh_muc";

        try {
            // Bước 2: tạo PreparedStatement để gửi câu truy vấn vào DB
            PreparedStatement ps = this.conn.prepareStatement(query);
            
            // Bước 3: Thực thi câu truy vấn
            ResultSet rs = ps.executeQuery();
            
            // Bước 4: bóc tách dữ liệu từ ResultSet
            while ( rs.next() ) {
                int id = rs.getInt("id");
                String ten = rs.getString("ten_danh_muc");

                DanhMuc danhMuc = new DanhMuc(id, ten);

                this.listDanhMuc.add(danhMuc);

                this.cbbDanhMuc.addItem(ten);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

