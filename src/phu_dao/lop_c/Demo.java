    private void cbbDanhMucActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbDanhMucActionPerformed
        int index = this.cbbDanhMuc.getSelectedIndex();
        DanhMuc danhMuc = this.listDanhMuc.get(index);

        String query = "SELECT * FROM sach WHERE danh_muc_id = ?";
        
        try {
            PreparedStatement ps = this.conn.prepareStatement(query);
            ps.setInt(1, danhMuc.getId());
            ResultSet rs = ps.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel) this.tblSach.getModel();
            model.setRowCount(0);
            
            while ( rs.next() ) {
                int id = rs.getInt("id");
                int soLuong = rs.getInt("so_luong");
                String ten = rs.getString("ten_sach");
                String ma = rs.getString("ma_sach");
                String ngayNhap = rs.getString("ngay_nhap");
                
                Sach sach = new Sach(id, ten, ma, soLuong, ngayNhap, danhMuc.getId());
                this.listSach.add(sach);
                
                model.addRow(new Object[] {
                    ma,
                    ten,
                    soLuong,
                    ngayNhap
                });                
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_cbbDanhMucActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = this.tblSach.getSelectedRow();
        if (row == -1) {
            return ;
        }

        String maSach = this.tblSach.getValueAt(row, 0).toString();
        String query = "DELETE FROM sach WHERE ma_sach = ?";
        try {
            PreparedStatement ps = this.conn.prepareStatement(query);
            ps.setString(1, maSach);
            ps.execute();
            JOptionPane.showMessageDialog(this, "Xóa thành công!");
            DefaultTableModel model = (DefaultTableModel) this.tblSach.getModel();
            model.removeRow(row);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

